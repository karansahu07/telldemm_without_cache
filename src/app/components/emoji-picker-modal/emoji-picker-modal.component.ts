  filteredEmojis: string[] = [];

  constructor(private modalCtrl: ModalController) {}

  ngOnInit() {
    this.loadRecentEmojis();
    this.filterEmojis();
  }

  loadRecentEmojis() {
    try {
      const stored = localStorage.getItem('recentEmojis');
      if (stored) {
        this.recentEmojis = JSON.parse(stored);
      }
    } catch (error) {
      console.error('Error loading recent emojis:', error);
    }
  }

  saveRecentEmoji(emoji: string) {
    // Remove if already exists
    this.recentEmojis = this.recentEmojis.filter(e => e !== emoji);
    
    // Add to beginning
    this.recentEmojis.unshift(emoji);
    
    // Keep only last 30
    if (this.recentEmojis.length > 30) {
      this.recentEmojis = this.recentEmojis.slice(0, 30);
    }
    
    try {
      localStorage.setItem('recentEmojis', JSON.stringify(this.recentEmojis));
    } catch (error) {
      console.error('Error saving recent emoji:', error);
    }
  }

  selectCategory(categoryName: string) {
    this.selectedCategory = categoryName;
    this.filterEmojis();
  }

  filterEmojis() {
    if (this.searchText.trim()) {
      // Search across all categories
      this.filteredEmojis = [];
      this.emojiCategories.forEach(category => {
        this.filteredEmojis.push(...category.emojis);
      });
    } else {
      // Show selected category
      const category = this.emojiCategories.find(c => c.name === this.selectedCategory);
      this.filteredEmojis = category ? category.emojis : [];
    }
  }

  onSearchChange() {
    this.filterEmojis();
  }

  selectEmoji(emoji: string) {
    this.saveRecentEmoji(emoji);
    this.modalCtrl.dismiss({
      emoji: emoji,
      selected: true
    });
  }

  dismiss() {
    this.modalCtrl.dismiss({
      selected: false
    });
  }
